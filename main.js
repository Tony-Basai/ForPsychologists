function createTest(questions, testId, finishId) {

    const container = document.getElementById(testId);
    const finish = document.getElementById(finishId);

    let answered = new Array(questions.length).fill(false);
    let score = 0;

    questions.forEach((item, i) => {

        const div = document.createElement("div");
        div.className = "question";

        let html = `<p>${item.q}</p>`;

        item.a.forEach((ans, j) => {
            html += `
            <label>
                <input type="radio" name="${testId}_q${i}"
                onclick="check_${testId}(${i}, ${j}, this)">
                ${ans}
            </label><br>`;
        });

        html += `<span id="${testId}_res${i}" class="result"></span>`;
        div.innerHTML = html;

        container.appendChild(div);
    });

    window["check_" + testId] = function(qIndex, aIndex, el) {

        if (answered[qIndex]) return;

        const res = document.getElementById(`${testId}_res${qIndex}`);

        if (aIndex === questions[qIndex].correct) {
            res.textContent = "✔";
            res.className = "result correct";
            score++;
        } else {
            res.textContent = "✖";
            res.className = "result wrong";
        }

        answered[qIndex] = true;

        const radios =
          document.getElementsByName(`${testId}_q${qIndex}`);
        radios.forEach(r => r.disabled = true);

        if (answered.every(v => v)) {
            finish.innerHTML =
                `✅ Тест завершён!<br>Ваш результат: ${score} из ${questions.length}`;
            finish.style.display = "block";
        }
    }
}

